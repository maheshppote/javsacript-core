let month = 7;

switch (month) {
  case 1:
    console.log("January");
    break;
  case 2:
    console.log("feb");
    break;
  case 3:
    console.log("March");
    break;
  case 4:
    console.log("april");
    break;
  case 5:
    console.log("May");
    break;
  case 6:
    console.log("Jun");
    break;
  case 7:
    console.log("July");
    break;

  default:
    console.log("No month required")
    break;
}
